/**
 * Options for Swagger UI setup
 */
export interface OpenApiOptions {
	/**
	 * Custom CSS for Swagger UI
	 */
	customCss?: string;

	/**
	 * Custom CSS URL
	 */
	customCssUrl?: string;

	/**
	 * Custom site title
	 */
	customSiteTitle?: string;

	/**
	 * Custom favicon URL
	 */
	customfavIcon?: string;

	/**
	 * Whether to use dark mode
	 */
	darkMode?: boolean;
}
